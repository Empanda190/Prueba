package registroaguacateria;

public class empleado {
    private String nombre;
    private int edad;

    private String casa;

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }

    public String getCasa() {
        return casa;
    }

    public void setNombre(String nombre) {
        nombre = nombre;
    }

    public void setEdad(int edad) {
        edad = edad;
    }


}
